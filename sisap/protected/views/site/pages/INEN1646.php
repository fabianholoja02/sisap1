<center><b>3. DISPOSICIONES GENERALES</b></center>
<br><br><b>3.1</b> Aplicación de las medidas antropométricas básicas
<br><br><b>3.1.1</b> Estatura (A). Permite la determinación de alturas mínimas en aberturas y puertas, y longitud
mínima del mobiliario en el que el usuario adopte la posición horizontal.
<br><br><b>3.1.2 </b>Alcance lateral del brazo (B). Esta medida es de utilidad para la determinación de altura a qué
colocar estantes laterales, aunque el usuario esté sentado.
<br><br><b>3.1.3 </b>Alcance vertical de asimiento (C). Sirve para determinar la altura a la que conviene instalar
interruptores, enchufes, controles, palancas, asas, estantes, perchas, etc.
<br><br><b>3.1.4</b> Anchura máxima del cuerpo (D). Se utiliza en el cálculo de anchuras para pasillos, puertas o
aberturas de acceso y mobiliario en el que el usuario adopte la posición horizontal.
<br><br><b>3.1.5</b> Altura de codo (E). Nos permite adjudicar una medida cómoda a la altura de mostradores,
tableros de cocina, tocadores, bancos de taller y otras superficies de trabajo de pie; los estudios
científicos la sitúan por debajo de la altura del codo.
<br><br><b>3.1.6</b> Altura de ojos (F). Sirve para fijar líneas de visión en teatros, auditorios y salas de conferencias,
los puntos donde instalar señalización y todo equipo de naturaleza visual, así como para establecer la
altura de mamparas y particiones en oficinas.
<br><br><b>3.1.7</b> Altura vertical en posición sedente (G). Es útil para definir el alcance del usuario a repisas,
anaqueles, controles, teclas, botones, etc., elevados, que sean manejados desde una posición sedente.
<br><br><b>3.1.8</b> Altura de ojos en posición sedente (H). Esta medida sirve para fijar líneas y ángulos de visión en
teatros, auditorios y salas de conferencias, y para establecer la altura de mamparas y particiones de
reducida privacidad en oficinas.
<br><br><b>3.1.9</b> Altura de rodilla (I). Sirve para fijar la distancia del suelo a la cara inferior de un escritorio, mesa
o mostrador en que una persona sentada deba necesariamente situar la parte inferior de su cuerpo. El
grado de proximidad usuario-elemento nos indicará si el factor determinante es la altura de rodilla o
la de muslo.
<br><br><b>3.1.10</b> Altura de muslo (J). Son importantes para el diseño de elementos interiores donde el usuario
sentado tenga que colocar sus piernas bajo la superficie de trabajo, por ejemplo: mostradores, mesas de
conferencias, de despacho, etc.
<br><br><b>3.1.11</b> Altura poplítea (K). Sirve para determinar la altura de la superficie de asiento respecto al nivel
del suelo, sobre todo en el punto más elevado de su parte anterior.
<br><br><b>3.1.12</b> Distancia nalga-poplíteo (L). Esta medida debe ser considerada en el diseño de asientos,
especialmente en cuanto a la ubicación de personas, superficies verticales frontales en bancos
corridos y longitud de éstos.
<br><br><b>3.1.13</b> Distancia nalga-rodilla (M). Se utiliza para establecer la distancia adecuada que debe separar la
parte superior del asiento de cualquier obstáculo físico u objeto que esté delante de las rodillas.
(Asientos fijos de auditorios, teatros, etc.).
<br><br><b>3.1.14</b> Anchura de hombros (N). Sirve para determinar tolerancias entre los asientos que rodean las
mesas, los que se disponen en fila en teatros y auditorios, y de holguras de paso en espacios públicos
y privados, así como para determinar el ancho útil y la holgura en asientos para varias personas.
<br><br><b>3.1.15</b> Altura en posición sedente erguida (O). Sirve para determinar la altura admisible a que debe
estar un obstáculo a partir de la superficie de asiento o del suelo; en este caso deberá sumarse la
altura a que ésta se encuentra. Se utiliza también para definir las alturas de espacios de
reducida privacidad en oficinas, y en las particiones que delimitan otros espacios funcionales.
<br><br><b>3.1.16</b> Altura de codo en reposo (P). Estos datos, en unión de otros, facilitan la determinación de
alturas de apoya-brazos, mostradores de trabajo, mesas y equipo especial.
<br><br><b>3.1.17</b> Anchura de caderas (Q). Es útil para establecer tolerancias en anchuras interiores de sillas,
asientos de bar y bancos corridos.
<br><br><b>3.1.18</b> Anchura de codos (R). Sirve para calcular las tolerancias para asientos alrededor de mesas de
conferencia, de comedor y de juego.
