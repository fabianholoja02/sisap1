<br><br><b>2.1.1</b> Estatura (A). Es la altura desde el suelo a la coronación de la cabeza, tomada en una persona de
pie, erguida y con la vista dirigida al frente.
<br><br><b>2.1.2</b> Alcance lateral del brazo (B). Es la distancia que se toma desde el eje central del cuerpo hasta la
superficie exterior de una barra sostenida por la mano derecha de una persona de pie y erguida, con
los brazos lo más estirados horizontalmente posible sin que experimente incomodidad alguna.
<br><br><b>2.1.3</b> Alcance vertical de asimiento (C). Se mide desde el suelo hasta la superficie horizontal de una
barra, que la mano derecha de la persona en observación, en pie y erguida, sostiene a la máxima
altura posible sin experimentar incomodidad alguna.
<br><br><b>2.1.4</b> Anchura máxima del cuerpo (D). Es la distancia horizontal máxima del cuerpo incluyendo los
brazos.
<br><br><b>2.1.5</b> Altura del codo (E), Es la altura tomada desde la depresión que forma la unión del brazo y
antebrazo hasta el suelo.
<br><br><b>2.1.6</b> Altura de ojos (F). Es la distancia vertical desde el suelo al punto de unión de los párpados
(comisura) en una persona de pie, erguida y con la vista dirigida al frente.
<br><br><b>2.1.7</b> Altura vertical en posición sedente (G). Es la altura que se toma a partir de la superficie de asiento
hasta la punta del dedo medio, teniendo brazo, mano y dedos completamente extendidos hacia arriba.
<br><br><b>2.1.8</b> Altura de ojos en posición sedente (H). Es la distancia que se mide desde el punto de unión
de los párpados (comisura interior) hasta la superficie del asiento.
<br><br><b>2.1.9</b> Altura de rodilla (I). Es la distancia vertical que se toma desde el suelo hasta la rótula de la
rodilla.
<br><br><b>2.1.10</b> Altura de muslo (J). Es la distancia vertical que se toma desde la superficie del asiento hasta la
parte superior del muslo.
<br><br><b>2.1.11</b> Altura poplítea (K). Es aquella que se toma desde el suelo hasta la zona inmediatamente
posterior de la rodilla de un individuo en posición sedente erguida, con la parte inferior de los muslos
y la posterior de las rodillas tocando apenas la superficie de asiento; éstas y los tobillos serán
perpendiculares entre sí.
<br><br><b>2.1.12</b> Distancia nalga-poplíteo (L). La longitud nalga-poplíteo es la distancia horizontal que se toma des-
de la superficie más saliente de la nalga hasta la cara posterior de la rodilla.
<br><br><b>2.1.13</b> Distancia nalga-rodilla (M). Es la distancia horizontal que se toma desde la superficie más
saliente de las nalgas hasta la cara frontal de la rótula de la rodilla.
<br><br><b>2.1.14</b> Anchura de hombros (N). Es la distancia horizontal que se toma entre las caras externas de
los hombros.
<br><br><b>2.1.15</b> Altura en posición sedente erguida (0). Es la distancia vertical desde la superficie del asiento
hasta la coronación de la cabeza, que se mide en un individuo sentado, pero con el cuerpo
incorporado.
<br><br><b>2.1.16</b> Altura de codo en reposo (P). Es la altura que se toma desde la superficie de asiento hasta la
punta inferior del codo.
<br><br><b>2.1.17</b> Anchura de caderas (Q). Anchura del cuerpo medida en la parte de las caderas en que sea
mayor. Esta medida se debe tomar en posición sedente.
<br><br><b>2.1.18</b> Anchura de codos (R). Es la distancia horizontal que se toma entre las superficies laterales de
los codos medida cuando están doblados, ligeramente apoyados contra el cuerpo y con los brazos
extendidos horizontalmente.
